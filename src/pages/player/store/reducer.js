export default function reducer(state = defaultState, action) {
  switch (action.type) {
    case actionTypes.CHANG_CURRENT_SONG:
      return state.set('currentSong', action.currentSong)
    case actionTypes.CHANG_CURRENT_SONG_INDEX:
      return state.set('currentSongIndex', action.currentSongIndex)
    case actionTypes.CHANG_PLAY_LIST:
      const isTrue = state.get('playList').some(item => action.currentSong.id === item.id)
      return isTrue ? state.set('playList', [...state.get('playList')]) : state.set('playList',[...state.get('playList'),action.currentSong])
    default:
      return state;
  }
}